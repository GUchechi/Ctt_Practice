#include <iostream>
using namespace std;
#include <cmath>

int main()
{
   

    cout << sqrt(25) << endl;
    cout << pow(2, 8) << endl;
    cout << remainder(10.5, 3.0) << endl;
    cout << fmod(10.5, 3.0) << endl;
    cout << fmin(10.5, 3.0) << endl;
    cout << ceil(10.5) << endl;
    cout << floor(10.5) << endl;
    cout << round(10.5) << endl;
    cout << trunc(10.5) << endl;

    return 0;
}